package net.daum.view.model;

public class ViewCategoryTag {

	private String tagCategory = "category";
	private String tagCategoryName = "categoryname";
	private String tagTab = "tab";
	private String tagTabName = "tabname";
	private String tagDirectory = "directory";
	private String tagParameter = "parameter";
	private String tagParamName = "paramname";
	private String tagParamValue = "paramvalue";
	
	public String getTagCategory() {
		return tagCategory;
	}
	
	public String getTagCategoryName() {
		return tagCategoryName;
	}

	public String getTagTab() {
		return tagTab;
	}
	
	public String getTagTabName() {
		return tagTabName;
	}
	
	public String getTagDirectory() {
		return tagDirectory;
	}
	
	public String getTagParameter() {
		return tagParameter;
	}
	
	public String getTagParamName() {
		return tagParamName;
	}
	
	public String getTagParamValue() {
		return tagParamValue;
	}
}
